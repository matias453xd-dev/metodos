public class Promedio {
    public static void main(String[] args) {
        System.out.print("Esta es una salida de texto");
        System.out.println("Esta salida si tiene");
        System.out.println("Aqui va una salida");

        int n = 10; // Declaración y asignación de n
        double prom = 6.7; // Declaración y asignación de promedio
        System.out.println("En un curso de " + n + " notas, el promedio es " + prom);

        double promedioReal = 10.0 / 3.0; // Cálculo del promedio real
        System.out.println("El promedio real es de " + promedioReal);
    }
}